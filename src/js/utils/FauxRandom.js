const setSeed = n => {
  n = Math.abs(n);
  n %= fauxArray.length;
  seed = n;
};

export function FauxRandom(n) {
  n ? setSeed(n) : setSeed(seed + 1);
  return fauxArray[seed];
}

// function returns a cos wave distribution between -1 and 1.
export function CosRandom() {
  return Math.cos(FauxRandom() * Math.PI * 2);
}
